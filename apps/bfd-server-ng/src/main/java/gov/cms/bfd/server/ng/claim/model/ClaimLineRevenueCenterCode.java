  /**
   * Convert from a database code.
   *
   * @param code database code
   * @return claim line revenue center code or empty Optional if code is null or blank
   */
  public static Optional<ClaimLineRevenueCenterCode> tryFromCode(String code) {
    if (code == null || code.isBlank()) {
      return Optional.empty();
    }
    return Arrays.stream(values()).filter(v -> v.getCode().equals(code)).findFirst();
  }

  /**
   * Converts this ANSI reason code into a list of FHIR {@link Coding} objects for multiple code
   * systems.
   *
   * @param deductibleCoinsuranceCode optional deductible coinsurance code
   * @return list of FHIR codings
   */
  public CodeableConcept toFhir(
      Optional<ClaimLineDeductibleCoinsuranceCode> deductibleCoinsuranceCode) {
    var codeableConcept =
        new CodeableConcept()
            .addCoding(
                new Coding()
                    .setSystem(SystemUrls.BLUE_BUTTON_CODE_SYSTEM_REVENUE_CENTER_CODE)
                    .setCode(getCode()))
            // Intentionally removed for now
            // .setDisplay(getDisplay()))
            .addCoding(new Coding().setSystem(SystemUrls.NUBC_REVENUE_CODES).setCode(getCode()));
    deductibleCoinsuranceCode.ifPresent(c -> codeableConcept.addCoding(c.toFhir()));
    return codeableConcept;
  }
