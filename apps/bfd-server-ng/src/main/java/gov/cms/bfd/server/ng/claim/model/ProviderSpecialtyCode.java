  private final String code;
  private final String display;
  private final String taxonomyCode;

  static ProviderSpecialtyCode fromCode(String code) {
    return Arrays.stream(values())
        .collect(Collectors.toMap(ProviderSpecialtyCode::getCode, e -> e))
        .get(code);
  }

  /**
   * Convert from a database code.
   *
   * @param code database code
   * @return provider specialty code
   */
  public static Optional<ProviderSpecialtyCode> fromCodeOptional(String code) {
    return Optional.ofNullable(fromCode(code));
  }

  CodeableConcept toFhir() {
    var codeableConcept =
        new CodeableConcept(
            new Coding().setSystem(SystemUrls.SYS_CLM_PRVDR_SPCLTY_CD).setCode(code));
    if (!taxonomyCode.isEmpty()) {
      codeableConcept.addCoding(
          new Coding().setSystem(SystemUrls.NUCC_PROVIDER_TAXONOMY).setCode(taxonomyCode));
    }
    return codeableConcept;
  }

  Extension toFhirExtension() {
    return new Extension(SystemUrls.EXT_CLM_PRVDR_SPCLTY_CD_URL)
        .setValue(new Coding(SystemUrls.SYS_CLM_PRVDR_SPCLTY_CD, code, display));
  }
